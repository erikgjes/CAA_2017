##10 Nodes
##Read in Barabasi Network and create random graphs
ba_10<-read.csv(text=getURL("https://raw.githubusercontent.com/erikgjes/CAA_2017/ba_10.csv",header=T))
g_10_0.2<-rgraph(10,tprob=0.2,mode="graph")
g_10_0.1<-rgraph(10,tprob=0.1,mode="graph")
g_10_0.4<-rgraph(10,tprob=0.4,mode="graph")

#Simple plotting of graphs
gplot(ba_10,gmode="graph")
gplot(g_10_0.2,gmode="graph",vertex.col="blue") #This  tie prob value comes from the gden of the Barabasi model
gplot(g_10_0.1,gmode="graph",vertex.col="dodgerblue")
gplot(g_10_0.4,gmode="graph",vertex.col="navyblue")

#Plotting of Tie Distributions
table.plot.function(ba_10)
table.plot.function(g_10_0.2)
table.plot.function(g_10_0.1)
table.plot.function(g_10_0.4)

##Bootstrap Analysis
#Degree
ba_boot_10<-NetBoot.totals(ba_10,6,degree) #data, number of sampling fraction (9 = 90%), centrality measure
random_boot_10<-NetBoot.totals(g_10_0.2,6,degree)
random_boot_10_half<-NetBoot.totals(g_10_0.1,6,degree)
random_boot_10_double<-NetBoot.totals(g_10_0.4,6,degree)

#Betweenness
ba_boot_10_between<-NetBoot.totals(ba_10,6,betweenness)
random_boot_10_between<-NetBoot.totals(g_10_0.2,6,betweenness)
random_boot_10_half_between<-NetBoot.totals(g_10_0.1,6,betweenness)
random_boot_10_double_between<-NetBoot.totals(g_10_0.4,6,betweenness)

#Bootstrap Plots - Degree
plot(ba_boot_10,type="n",ylim=c(0,1),xaxt="n"
     ,xlab="Sampling Fraction (%)",ylab="Correlation",main="Node Size 10 (Degree)")
axis(1,at=c(9,8,7,6,5,4,3,2,1),labels=rev(c(90,80,70,60,50,40,30,20,10)))
